    /**
     * 计算并写回 bmi、totalScore、level
     *
     * @param test      记录对象
     * @param gender    "男" / "女"
     * @param gradeName 年级名称，如 "高一"、"高二"、"高三"
     */
    public void computeAndFill(PhysicalTest test, String gender, String gradeName) {
        int grade = toGradeLevel(gradeName);
        boolean male = "男".equals(gender);

        // BMI 自动计算
        double bmiScore = 0;
        if (test.getHeight() != null && test.getWeight() != null && test.getHeight() > 0) {
            double h = test.getHeight() / 100.0;
            double bmi = test.getWeight() / (h * h);
            test.setBmi(Math.round(bmi * 10.0) / 10.0);
            bmiScore = scoreBmi(bmi, male, grade);
        }

        double lungScore   = lookupHigh(toLungVal(test.getLungCapacity()), lungTable(male, grade));
        double sprintScore = lookupLow(test.getSprint50m(), sprintTable(male, grade));
        double sitScore    = lookupHigh(test.getSitReach(), sitTable(male, grade));
        double jumpScore   = lookupHigh(test.getStandingJump(), jumpTable(male, grade));
        double strengthScore;
        double endureScore;

        if (male) {
            strengthScore = lookupHigh(toDouble(test.getPullUps()), pullTable(grade));
            endureScore   = lookupLow(test.getRun1000m(), run1000Table(grade));
        } else {
            strengthScore = lookupHigh(toDouble(test.getSitUps()), situpTable(grade));
            endureScore   = lookupLow(test.getRun800m(), run800Table(grade));
        }

        double total = bmiScore    * 0.15
                     + lungScore   * 0.15
                     + sprintScore * 0.20
                     + sitScore    * 0.10
                     + jumpScore   * 0.10
                     + strengthScore * 0.10
                     + endureScore * 0.20;

        total = Math.round(total * 10.0) / 10.0;
        test.setTotalScore(total);
        test.setLevel(toLevel(total));
    }

    // =====================================================================
    // 内部工具
    // =====================================================================

    private int toGradeLevel(String name) {
        if (name == null) return 1;
        if (name.contains("高三") || name.contains("三年级")) return 3;
        if (name.contains("高二") || name.contains("二年级")) return 2;
        return 1;
    }

    private String toLevel(double score) {
        if (score >= 90) return "优秀";
        if (score >= 80) return "良好";
        if (score >= 60) return "及格";
        return "不及格";
    }

    /** BMI 按年级精确判断（教育部2014标准） */
    private double scoreBmi(double bmi, boolean male, int grade) {
        // 正常范围上下限（按年级）
        double loNormal = male
                ? (grade == 1 ? 16.5 : grade == 2 ? 16.8 : 17.3)
                : (grade == 1 ? 16.5 : grade == 2 ? 16.9 : 17.1);
        double hiNormal = male
                ? (grade == 1 ? 23.2 : grade == 2 ? 23.7 : 23.8)
                : (grade == 1 ? 22.7 : grade == 2 ? 23.2 : 23.3);
        double hiOverweight = male
                ? (grade == 1 ? 26.3 : grade == 2 ? 26.5 : 27.3)
                : (grade == 1 ? 25.2 : grade == 2 ? 25.4 : 25.7);

        if (bmi >= loNormal && bmi <= hiNormal) return 100;
        if (bmi > hiOverweight) return 60;   // 肥胖
        return 80;   // 低体重 或 超重
    }

    /** 高值优查找：值 >= 阈值（降序）→ 取对应得分 */
    private double lookupHigh(Double value, double[][] table) {
        if (value == null) return 0;
        for (double[] row : table) {
            if (value >= row[0]) return row[1];
        }
        return 10;
    }

    /** 低值优查找：值 <= 阈值（升序）→ 取对应得分 */
    private double lookupLow(Double value, double[][] table) {
        if (value == null) return 0;
        for (double[] row : table) {
            if (value <= row[0]) return row[1];
        }
        return 10;
    }

    private Double toLungVal(Integer v) { return v == null ? null : (double) v; }
    private Double toDouble(Integer v)  { return v == null ? null : (double) v; }

    private double[][] lungTable(boolean male, int g) {
        return male ? (g==3?LUNG_M3 : g==2?LUNG_M2 : LUNG_M1)
                    : (g==3?LUNG_F3 : g==2?LUNG_F2 : LUNG_F1);
    }
    private double[][] sprintTable(boolean male, int g) {
        return male ? (g==3?SPRINT_M3 : g==2?SPRINT_M2 : SPRINT_M1)
                    : (g==3?SPRINT_F3 : g==2?SPRINT_F2 : SPRINT_F1);
    }
    private double[][] sitTable(boolean male, int g) {
        return male ? (g==3?SIT_M3 : g==2?SIT_M2 : SIT_M1)
                    : (g==3?SIT_F3 : g==2?SIT_F2 : SIT_F1);
    }
    private double[][] jumpTable(boolean male, int g) {
        return male ? (g==3?JUMP_M3 : g==2?JUMP_M2 : JUMP_M1)
                    : (g==3?JUMP_F3 : g==2?JUMP_F2 : JUMP_F1);
    }
    private double[][] pullTable(int g) {
        return g==3?PULL_M3 : g==2?PULL_M2 : PULL_M1;
    }
    private double[][] situpTable(int g) {
        return g==3?SITUP_F3 : g==2?SITUP_F2 : SITUP_F1;
    }
    private double[][] run1000Table(int g) {
        return g==3?RUN1000_M3 : g==2?RUN1000_M2 : RUN1000_M1;
    }
    private double[][] run800Table(int g) {
        return g==3?RUN800_F3 : g==2?RUN800_F2 : RUN800_F1;
    }
